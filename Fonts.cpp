// Helper functions
void getNormalFontChar(char c, uint8_t* buffer) {
    int index = 0;
    
    if (c == ' ') {
        index = 0;
    }
    else if (c >= 'A' && c <= 'Z') {
        index = c - 'A' + 1;  // A=1, B=2, ... Z=26
    }
    else if (c >= '0' && c <= '9') {
        index = 31 + (c - '0');  // 0=31, 1=32, ... 9=40
    }
    else if (c >= 'a' && c <= 'z') {
        index = 41 + (c - 'a');  // a=41, b=42, ... z=66
    }
    else if (c == '.') {
        index = 27;
    }
    else if (c == '\'') {
        index = 28;
    }
    else if (c == ':') {
        index = 29;
    }
    else {
        index = 0;  // Default to space
    }
    
    // Ensure index is within bounds
    if (index < 0 || index >= 80) {
        index = 0;
    }
    
    for (int i = 0; i < 5; i++) {
        buffer[i] = pgm_read_byte(&myfont[index][i]);
    }
}

void getBigFontChar(int num, uint8_t* buffer) {
    if (num < 0 || num >= 10) return;
    
    for (int i = 0; i < 20; i++) {
        buffer[i] = pgm_read_byte(&mybigfont[num][i]);
    }
}

